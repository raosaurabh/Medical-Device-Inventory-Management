    private void completeMaintainenceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeMaintainenceBtnActionPerformed
        int selectedRow = maintainenceTbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row");
            return;
        }
        MaintainenceTestWorkRequest mtwr = (MaintainenceTestWorkRequest) maintainenceTbl.getValueAt(selectedRow, 0);
        mtwr.setStatus("Maintainence Completed");
        
        mtwr.setReceiver(useraccount);
        mtwr.getInventoryItem().setUsageCounter(0);
        mtwr.getInventoryItem().setMaintainenceStatus("MaintainenceCompleted");
        System.out.println("The maintainence completed successfully");
        populateMaintainenceTable();

    }//GEN-LAST:event_completeMaintainenceBtnActionPerformed


