    private void populateMaintainenceTable() {
        DefaultTableModel model = (DefaultTableModel) maintainenceTbl.getModel();
        model.setRowCount(0);
        for (WorkRequest request : supplierOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof MaintainenceTestWorkRequest) {
                MaintainenceTestWorkRequest omTest = (MaintainenceTestWorkRequest) request;
                Object[] row = new Object[5];
                row[0] = omTest;
                row[1] = omTest.getSender();
                row[2] = omTest.getReceiver() == null ? null : omTest.getReceiver().getEmployee().getName();
                row[3] = omTest.getInventoryItem().getOrderitem().getProductItem().getProductName();
                row[4] = omTest.getStatus();
                model.addRow(row);
            }

        }

    }
